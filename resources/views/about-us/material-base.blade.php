                        <p>Навчальний стенд №&nbsp;1. Система позиціювання B&amp;R: Інтерактивне програмування на стійці ЧПУ CNC-3D.</p>
                        <p>Навчальний стенд №&nbsp;2. Комп`ютерно-інтегрована система автоматизації B&amp;R2005: Настроювання ПІД-регулятора САУ.</p>
                        <p>Навчальний стенд №&nbsp;3. Інтегрований привід ACOPOS: Інтерактивне настроювання динамічних параметрів системи при різних навантаженнях серводвигуна.</p>
                        <p>Навчальний стенд №&nbsp;4. Панель керування оператора Power Panel PP21/41: Програмування завдання візуалізації, керування, позиціювання і зв'язки.</p>
                        <p>Навчальний стенд №&nbsp;5. Комп`ютерно-інтегрований стенд РТК: Програмування завдання керування&nbsp; роботом «МП-9С» на базі PLC B&amp;R2003.</p>
                        <p>Навчальний стенд №&nbsp;6. Стенд електропневмоавтоматики з набором елементів: Моделювання завдання автоматизації пневмопривода.</p>
                        <p>Навчальний стенд №&nbsp;7. Електрогідравлічний стенд із набором елементів: Моделювання завдань верстатного гідроприводу.</p>
                        <p>Навчальний стенд №&nbsp;8. Програмно-технічний комплекс «КОНТАР-КМ800»: Проектування системи керування і програмування приладів комплексу в програмному середовищі KONGRAPH.</p>
                        <p>Навчальний стенд №&nbsp;9. Комп`ютерно-інтегрована система керування РТК на базі мікроконтролера EV8031/AVR: Програмування завдання керування роботами ПР-5.</p>
                        <p>У лабораторії автоматизованого електропривода змонтовано два стенди з новітніми системами частотного керування асинхронними двигунами фірми АВВ (Швеція), комп’ютерно-інтегрований стенд для дослідження системи автоматичного керування електродвигунами постійного струму, стенди із сучасними системами керування мікроприводами та інші.</p>
