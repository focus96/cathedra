                        <p>Навчальні приміщення оснащено найсучаснішими стендами та приладами. Так, в лабораторії електроніки і мікросхемотехніки застосовуються чотири унікальних сучасних стенди, кожний з яких забезпечує виконання 32 лабораторних робіт. У лабораторії комп’ютерно-інтегрованого керування при сприянні Краматорської ТЕЦ встановлено обладнання з модемним зв’язком «Кафедра-ТЕЦ», яке дозволяє студентам вивчати роботу кращої в СНД системи обліку витрати енергоносіїв. Кафедра має спеціалізовані мікропроцесорні системи для комп’ютерно-інтегрованих технологій від фірми B&amp;R (Австрія) і науково-виробничого центра «Спецсистемы» (Республіка Білорусь). Функціонує лабораторія гідро- пневмоавтоматики європейського рівня, для якої придбано стенди всесвітньо відомої фірми FESTO-Didaсtiс (Німеччина). Стенди укомплектовані елементами, які застосовуються у виробництві і дозволяють досліджувати різноманітні схемотехнічні рішення з використанням комп’ютерної та мікропроцесорної техніки. Разом з цим обладнанням кафедра отримала дидактичні матеріали, каталоги продукції та програмне забезпечення фірми «FESTO». Також впроваджено у навчальний процес три навчальних стенди Московського заводу теплової автоматики «КОНТАР-КМ800», що містять у собі контролери MC8, MC5, MR8, датчики температури, вологості та електропривід LM24SR (Belimo, Швейцарія), який призначено для керування повітряними заслінками, а також програмне забезпечення: інструментальна система програмування KONGRAF, програмний засіб CONSOLE для завантаження та роботи з приладами, система моніторингу KONTAR-SCADA. На базі цих приладів створено модель системи керування для вентиляції, кондиціювання та опалювання приміщення.</p>
